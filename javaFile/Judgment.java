public class Judgment {
	private Integer judgmentid;
	private Integer judgmenttype;
	private Integer judgmentlevel;
	private Integer jrange;
	
	public Integer getJudgmentid() {
		return judgmentid;
	}

	public void setJudgmentid(Integer judgmentid) {
		this.judgmentid = judgmentid;
	}
	
	public Integer getJudgmenttype() {
		return judgmenttype;
	}

	public void setJudgmenttype(Integer judgmenttype) {
		this.judgmenttype = judgmenttype;
	}
	
	public Integer getJudgmentlevel() {
		return judgmentlevel;
	}

	public void setJudgmentlevel(Integer judgmentlevel) {
		this.judgmentlevel = judgmentlevel;
	}
	
	public Integer getJrange() {
		return jrange;
	}

	public void setJrange(Integer jrange) {
		this.jrange = jrange;
	}
}
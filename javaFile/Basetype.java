public class Basetype {
	private Integer basetypeid;
	private String basetypename;
	private String basetypecode;
	private String remark;
	
	public Integer getBasetypeid() {
		return basetypeid;
	}

	public void setBasetypeid(Integer basetypeid) {
		this.basetypeid = basetypeid;
	}
	
	public String getBasetypename() {
		return basetypename;
	}

	public void setBasetypename(String basetypename) {
		this.basetypename = basetypename;
	}
	
	public String getBasetypecode() {
		return basetypecode;
	}

	public void setBasetypecode(String basetypecode) {
		this.basetypecode = basetypecode;
	}
	
	public String getRemark() {
		return remark;
	}

	public void setRemark(String remark) {
		this.remark = remark;
	}
}